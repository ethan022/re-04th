# 딥러닝이란?
# 컴퓨터가 사람처럼 학습하는 방법

# 예시로 이해하기
# 아기가 고양이를 배우는 과정:
# 1. 엄마가 "고양이야~" 하며 여러 고양이 사진을 보여줌
# 2. 아기는 점점 고양이의 특징을 기억함 (귀가 뾰족, 수염, 꼬리...)
# 3. 나중에 새로운 고양이를 봐도 "고양이!"라고 알아봄

# 딥러닝도 똑같습니다!
# 1. 컴퓨터에게 많은 고양이 사진을 보여줌
# 2. 컴퓨터가 스스로 고양이의 특징을 찾아냄
# 3. 새로운 사진을 봐도 고양이인지 아닌지 구분할 수 있음


# 왜 '딥(Deep)'러닝일까?
# 전통적인 방법: 간단한 1단계 학습
# 예) 사진 → [분석] → 결과

# 딥러닝: 여러 단계로 깊게 학습
# 예) 사진 → [1단계: 선 찾기] → [2단계: 모양 찾기] →
#          [3단계: 부분 찾기] → [4단계: 전체 인식] → 결과

# 단계가 많을수록 (깊을수록) 더 복잡한 것을 배울 수 있음!


# 머신러닝 vs 딥러닝 쉽게 비교

# 머신러닝: 사람이 알려줘야 함
# 예) 고양이 찾기
# - 사람: "귀가 뾰족한지 확인해!"
# - 사람: "수염이 있는지 확인해!"
# - 사람: "꼬리가 긴지 확인해!"
# - 컴퓨터: 이 기준으로 판단

# 딥러닝: 컴퓨터가 알아서 찾음
# - 컴퓨터: 많은 사진을 보고 스스로 특징을 찾아냄
# - 컴퓨터: "아, 귀 모양이 중요하구나!"
# - 컴퓨터: "수염 개수도 중요하네!"


# 비유로 이해하기

# 머신러닝 = 요리책 보고 요리하기
# - 레시피(규칙)를 사람이 만들어줌
# - 재료가 바뀌면 새 레시피 필요

# 딥러닝 = 요리 영상 많이 보고 스스로 배우기
# - 많은 요리를 보면서 패턴을 스스로 찾음
# - 새로운 요리도 응용 가능


# 언제 딥러닝을 쓸까?

# 딥러닝이 좋을 때:
# ✓ 사진, 영상, 음성처럼 복잡한 데이터
# ✓ 데이터가 엄청 많을 때 (수만 개 이상)
# ✓ 사람이 규칙을 설명하기 어려울 때
# 예) "고양이가 뭐야?"라고 물으면 설명하기 어렵지만
#     사진만 보면 바로 알 수 있음

# 머신러닝이 좋을 때:
# ✓ 데이터가 적을 때 (수천 개)
# ✓ 왜 그런 결과가 나왔는지 설명해야 할 때
# ✓ 컴퓨터 성능이 낮을 때


# 딥러닝이 가능해진 이유

# 1. 데이터 폭발
# - 스마트폰, 인터넷으로 엄청난 양의 사진/영상/텍스트 생성
# - 예) 유튜브에 매일 올라오는 영상 = 500시간!

# 2. 컴퓨터 성능 향상
# - GPU(그래픽 카드)가 빠른 계산을 도와줌
# - 예전에는 몇 주 걸렸던 학습이 지금은 몇 시간!


# 딥러닝 기본 용어 (쉽게!)

# 신경망 (Neural Network)
# = 뇌를 흉내낸 컴퓨터 프로그램
# 뇌의 뉴런처럼 연결된 작은 계산기들의 집합

# 층 (Layer)
# = 신경망의 각 단계
# 예) 1층에서 선 찾기 → 2층에서 모양 찾기 → 3층에서 물체 찾기

# 학습 (Training)
# = 컴퓨터가 데이터를 보면서 배우는 과정
# 예) 고양이 사진 1000장을 보면서 특징을 배움

# 에폭 (Epoch)
# = 모든 데이터를 한 번 다 본 것
# 예) 사진 1000장을 한 번 다 보면 1에폭

# 배치 (Batch)
# = 한 번에 학습하는 데이터 묶음
# 예) 1000장을 한 번에 못 보니까 10장씩 나눠서 학습


# PyTorch 설치 확인
# PyTorch = 딥러닝을 쉽게 만들어주는 도구

import torch
import torchvision

print("=== PyTorch 설치 확인 ===")
print(f"PyTorch 버전: {torch.__version__}")
print(f"Torchvision 버전: {torchvision.__version__}")

# GPU 사용 가능 여부
# GPU = 딥러닝을 빠르게 해주는 그래픽 카드
print(f"\nGPU 사용 가능: {torch.cuda.is_available()}")
if torch.cuda.is_available():
    print("→ 딥러닝 학습이 빠르게 가능합니다!")
else:
    print("→ CPU로 학습합니다 (조금 느릴 수 있어요)")

# 간단한 텐서 만들기
# 텐서 = 숫자를 담는 상자 (배열과 비슷)
print("\n=== 간단한 텐서 만들기 ===")
x = torch.randn(3, 3)  # 3x3 크기의 랜덤 숫자
print("3x3 텐서 (랜덤 숫자):")
print(x)

# 텐서 연산 예시
print("\n=== 텐서 연산 예시 ===")
a = torch.tensor([1, 2, 3])
b = torch.tensor([4, 5, 6])
print(f"a = {a}")
print(f"b = {b}")
print(f"a + b = {a + b}")  # 덧셈
print(f"a * b = {a * b}")  # 곱셈

print("\n✓ PyTorch가 정상적으로 작동합니다!")
